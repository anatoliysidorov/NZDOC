SELECT COL_ID,
  COL_CREATEDBY,
  COL_CREATEDDATE,
  COL_ISDELETED,
  COL_LOCKEDBY,
  COL_LOCKEDDATE,
  COL_LOCKEDEXPDATE,
  COL_MODIFIEDBY,
  COL_MODIFIEDDATE,
  COL_OWNER,
  COL_PEOPLEINVOVLED,
  COL_RESOLUTION,
  COL_SUMMARY,
  COL_WORKACTIVITYCASE,
  COL_WORKACTIVITYTYPE,
  COL_WORKACTIVITYTASK,
  COL_CUSTOMDATA,
  COL_HOURSSPENT,
  u.NAME AS CreatedByName
FROM TBL_DCM_WORKACTIVITY w
LEFT JOIN VW_USERS u ON w.col_createdby = u.ACCESSSUBJECTCODE